func hammingDistance(x, y int) int {

	return onesCount32(x ^ y)
}

func onesCount32(x int) int {
	return int(pop8tab[x>>24] + pop8tab[x>>16&0xff] + pop8tab[x>>8&0xff] + pop8tab[x&0xff])
}
